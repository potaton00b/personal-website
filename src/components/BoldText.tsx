// Renders a string with **bold** markers as <strong> elements.
// Usage: <BoldText text="Hello **world**" />
export default function BoldText({ text }: { text: string }) {
  const parts = text.split(/\*\*(.*?)\*\*/g);
  return (
    <>
      {parts.map((part, i) =>
        i % 2 === 1 ? <strong key={i}>{part}</strong> : part
      )}
    </>
  );
}
